#include "libft.h"

int		ft_atoi(const char *nptr)
{
	int res;
	int i;
	int neg;

	i = 0;
	res = 0;
	neg = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if ((nptr[i] == '+' || nptr[i] == '-') && (nptr[i + 1] >= '0' &&
			nptr[i + 1] <= '9'))
	{
		if (nptr[i] == '-')
			neg = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		res = res * 10 + (nptr[i] - '0');
		i++;
	}
	return (neg * res);
}
