#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (*s)
	{
		s++;
		j++;
	}
	while (j >= 0)
	{
		if (*s == c)
			return ((char *)s);
		s--;
		j--;
	}
	return (NULL);
}
